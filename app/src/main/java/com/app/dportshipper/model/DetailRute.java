package com.app.dportshipper.model;

public class DetailRute {

    private int imgTruck;
    private String detailKotaAsal;
    private String detailKotaTujuan;
    private String detailTglPengiriman;
    private String detailTipeKendaraan;
    private String detailNomorPlat;
    private String detailBobotMuatan;

    public DetailRute(){}

    public int getImgTruck() {
        return imgTruck;
    }

    public void setImgTruck(int imgTruck) {
        this.imgTruck = imgTruck;
    }

    public String getDetailKotaAsal() {
        return detailKotaAsal;
    }

    public void setDetailKotaAsal(String detailKotaAsal) {
        this.detailKotaAsal = detailKotaAsal;
    }

    public String getDetailKotaTujuan() {
        return detailKotaTujuan;
    }

    public void setDetailKotaTujuan(String detailKotaTujuan) {
        this.detailKotaTujuan = detailKotaTujuan;
    }

    public String getDetailTglPengiriman() {
        return detailTglPengiriman;
    }

    public void setDetailTglPengiriman(String detailTglPengiriman) {
        this.detailTglPengiriman = detailTglPengiriman;
    }

    public String getDetailTipeKendaraan() {
        return detailTipeKendaraan;
    }

    public void setDetailTipeKendaraan(String detailTipeKendaraan) {
        this.detailTipeKendaraan = detailTipeKendaraan;
    }

    public String getDetailNomorPlat() {
        return detailNomorPlat;
    }

    public void setDetailNomorPlat(String detailNomorPlat) {
        this.detailNomorPlat = detailNomorPlat;
    }

    public String getDetailBobotMuatan() {
        return detailBobotMuatan;
    }

    public void setDetailBobotMuatan(String detailBobotMuatan) {
        this.detailBobotMuatan = detailBobotMuatan;
    }
}
